import { Schema, model } from 'mongoose';

const AdminSchema = new Schema({
    name: {
        type: String,
        required: true,
        trim: true
    },
    email: {
        type: String,
        required: true,
        unique: true,
        lowercase: true,
        trim: true
    },
    password: {
        type: String,
        required: true,
    }
}, { timestamps: true });

export const Admin = model('Admin', AdminSchema);
